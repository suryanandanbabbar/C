#include <stdio.h>

int main()
{
    // Leaves 4 spaces
    printf("%4d\n", 1);
    printf("%4d\n", 12);
    printf("%4d\n", 123);
    printf("%4d\n", 1234);

    // Displays Slash
    printf("This prints this \\ only once.\n");

    // Quotes
    printf("Displays \'single\' quotes\n"); // Single Quotes
    printf("Displays \"double\" quotes\n"); // Double Quotes

    return 0;
}